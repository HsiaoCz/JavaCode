package day01;

public class vars {
   public static void main(String[] args) {
      // 变量
      // java的编译器将代码转换成字节码
      // 字节码给jvm执行
      String a = "Hello Java";
      System.out.println(a);
      // 所谓的变量就是可以改变的向量存储
      int b = 12;
      System.out.println(b);
      boolean f = false;
      System.out.println(f);

      // 声明变量
      String s;
      s = "Hi java";
      System.out.println(s);
   }
}
