package day01;

public class operator_2 {
    public static void main(String[] args) {
        // 关系运算符，比较两个数据之间关系的运算符
        // == != > >= < <=
        // 比较的结果为boolean
        int a = 10;
        int b = 11;
        System.out.println(a > b);
        System.out.println(a >= b);
        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(b < a);
        System.out.println(b <= a);
    }
}
